<h1>Free printing of posters</h1>

<p>
Many participants have asked for a local printing service 
for posters in Aalborg to avoid traveling with posters.
As an extra service to presenters of posters at useR! 2015
we therefore offer to <b>print your A0 posters</b> prior to the conference
<b>free of charge!</b>
</p>

<p>
In order to have your poster printed by us, we need to receive
your poster as a <b>pdf-file no later than June 14 23:59 (CET)</b>.
</p>

<p>
The poster must be in <b>A0 format (84.1 x 118.9 cm, or 33.1 in x 46.8 inches)
and formatted in portrait format</b>. 
</p>

<p>
There will only be printed one copy of each poster. Hence,
make sure that the file you sent is the <b>final version!</b>
</p>

<p>
To make use of this service email your poster as an <b>attachment (pdf-file)</b>
to our secretary Amra Ibrisevic
(<a href="mailto:amra@math.aau.dk?subject=Poster">amra@math.aau.dk</a>)
before June 15th.
</p>
