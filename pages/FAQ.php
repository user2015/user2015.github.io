<p>There will be whiteboard and pens.</p>
</blockquote></li>
</ul>

<h3 id="presentations">Presentations</h3>
<ul>
<li>What's the allocated time per presentation?
<blockquote>
<p>For the invited talks we have scheduled 45 mins of presentation, which will
be followed by 10-15 mins for questions.</p>
<p>The contributed talks will last 15 mins followed by 2-3 mins for question.</p>
<p>Lightning talks are 5 mins with exactly 15 slides (20 secs per slide).
There will be no time for questions after the lightning talks (may be asked in person 
during the subsequent poster sesssion).</p>
<p>Presenters of posters should be around to discuss and explain their work during
the poster session (Wednesday evening, 18:00 - 21:00).
</blockquote></li>
<li>Can I use the conference logo on posters and in presenations?
<blockquote>
Of course! Use it as you would use the R logo: <a href="./gfx/useR2015.png">High resolution logo</a>.
</blockquote></li>
<li>
What are the requirements to length and format for oral presentations?
<blockquote>
<p>Kaleidoscope and Focus contributed talks, will be scheduled for 15 minutes,
followed by 2-3 minutes discussion. It is possible to use either your own computer or the one in the room.

<p>There will be laptops in each conference room that can be used during the presentation.
These computers will come with Windows, Microsoft Office (for PowerPoint) and Adobe Reader (for pdf slides).
If your presentation contains demostration in R or web browser we recommend that you bring your own laptop.
The projectors connect via VGA port. For the <em>Kaleidoscope sessions</em> the aspect ratio is 16:9 (1920 x 1080) 
and for <em>Focus Sessions</em> the aspect ratio is 4:3 (1024 x 768).
</p>
</blockquote>
</li>
<li>What are the requirements to length and format for Lightning talks?
<blockquote>
Presenters of Lightning talks have <em>5 minutes</em> at their disposal to present <em>15 slides</em> (<i>exactly 20 seconds per
slide</i>).</p>
<p>
We will ask you to prepare/compile your slides as a pdf-file. This file should be send to the organisers prior to the conference
(<a href="mailto:user2015@math.aau.dk">user2015@math.aau.dk</a>).
The <em>deadline</em> for submitting your lightning talk (pdf-file) is <em>Tuesday 30th of June, 12:00 (CET)</em>. 
The projector's aspect ratio is 4:3 (1024 x 768).</p>
</blockquote>
</li>
</ul>

