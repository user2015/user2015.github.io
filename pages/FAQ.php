<h3 id="tutorial">Tutorials</h3>
<ul>
<li>Do I have to sign up for the tutorials?
<blockquote>
No as useR! 2015 participant you just show and take a seat.
There should be sufficient room for everybody to have their first pick
<em>(We have allocated the tutorials into rooms based on a survey sent out
to registrants who signed up before June 1st).</em>
</blockquote></li>
<li>Where can I buy food and drinks during Tutorial Tuesday?
<blockquote>
Fruit, Sandwiches, cake, coffe and beverages will be served during the day - 
also included in your conference fee.
</blockquote></li>
<li>Should I bring my own laptop to the conference?
<blockquote>
If you take part in the tutorials during Tutorial Tuesday you must bring your own laptop.
There will be a great deal of hands-on excercises.
</blockquote></li>
<li>What will there be in the tutorial rooms?
<blockquote>
<p>The rooms vary in size - see the <a href="./tutorials">tutorial page</a> for details on number of seats.</p>
