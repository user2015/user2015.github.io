<h2 id="location">Location of booths</h2>

<p>The exhibition area will be located next to the main theatre hall,
where the opening remarks, plenary talks, sponsor session and closing
remarks will be given. Also, all breaks during the conference (coffee
breaks and lunches) will be served in the foyer next door to the
exhibition area.</p>

<p>The poster walls for mounting conference posters will be in the same
rooms as the booths, meaning that the poster session (Wednesday 18:00
- 21:00, 6pm - 9pm) will take next to the booths. This will allow the
participants to engage in discussions with the exhibitors during the
poster session.</p>

