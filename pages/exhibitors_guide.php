<h2 id="description">Description of booth</h2>
The booths for useR! 2015 all come with the following equipment and specifications:
<ul>
<li>rear and 1 meter dividing walls - white HDF (hard plate): thickness 4 mm</li>
<li>we offer Power Strips (easy-to-remove adhesive pads) for hanging posters on walls</li>
<li>hooks in four different lengths (5 cm, 25 cm, 50 cm and 75 cm) may be borrowed at the organiser’s office</li>
<li>one socket 230V / 2.000 kW</li>
<li>spotlights</li>
<li>wireless internet</li>
<li>table high/low (diameter 70 cm/height 110 cm)</li>
<li>square table (76 × 182 cm)</li>
<li>white tablecloth</li>
<li>basic chairs</li>
<li>zig-zag brochure rack</li>
</ul>

